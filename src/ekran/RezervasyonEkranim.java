 public static void rezervasyonOlustur(int masano, String rezerv_saat, String rezerv_tarih, Connection baglanti) {
  try {
   stmt = baglanti.createStatement();
   String sql = "INSERT INTO rezervasyon (tarihi,saat,masano) "
           + "VALUES ('" + rezerv_tarih + "','" + rezerv_saat + "', '" + masano + "');";
   stmt.executeUpdate(sql);
   stmt.close();
   baglanti.commit();
   JOptionPane.showMessageDialog(null, "Rezervasyon Başarıyla Oluşturuldu.", "Bilgilendirme", JOptionPane.INFORMATION_MESSAGE);
  } catch (Exception e) {
   JOptionPane.showMessageDialog(null, "Rezervasyon Oluşturulamadı", "Error", JOptionPane.ERROR_MESSAGE);
   e.printStackTrace();
  }

 }

