 public static void zamaniOlustur() {
  for (int i = 0; i < 24; i++) {
   if (i < 10) {
    saatCombo.addItem(String.valueOf(0) + String.valueOf(i));
   } else {
    saatCombo.addItem(String.valueOf(i));
   }
  }

  for (int j = 0; j <= 59; j++) {
   if (j < 10) {
    dakikaCombo.addItem(String.valueOf(0) + String.valueOf(j));
   } else {
    dakikaCombo.addItem(String.valueOf(j));
   }
  }

 }

 public static void tarihOlustur() {
  for (int i = 1; i <= 31; i++) {
   if (i < 10) {
    gunCombo.addItem(String.valueOf(0) + String.valueOf(i));
   } else {
    gunCombo.addItem(String.valueOf(i));
   }
  }

  for (int j = 0; j <= 12; j++) {
   if (j < 10) {
    ayCombo.addItem(String.valueOf(0) + String.valueOf(j + 1));
   } else {
    ayCombo.addItem(String.valueOf(j));
   }
  }

  for (int k = 0; k < 3; k++) {
   yilCombo.addItem(String.valueOf(2015 + k));
  }

 }

 public static void masaDoldur() {
 Connection baglanti = null;
  baglanti = VeriTabani.veritabaninaBaglan();
  masalar = VeriTabani.masaCek();
  String[] masaDizisi = new String[masalar.size()];
  for (int i = 0; i < masalar.size(); i++) {
   masaDizisi[i] = (String.valueOf(masalar.get(i).getMasaNo()));
  }
  
  comboboxModel = new DefaultComboBoxModel(masaDizisi);
  comboboxRezervasyon.setModel(comboboxModel);
  try {
   VeriTabani.baglantiKapat(baglanti);
  } catch (Exception e) {
  }
 }

 
